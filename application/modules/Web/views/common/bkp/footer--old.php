    function updateQuantity(action, element) {
        var qtyInput = element.parentNode.querySelector('.qty-val'); // Get the input field
        var currentValue = parseInt(qtyInput.value); // Get the current value

        if (action === 'decrease' && currentValue > 1) {
            qtyInput.value = currentValue - 1; // Decrease value
        } else if (action === 'increase') {
            qtyInput.value = currentValue + 1; // Increase value
        }
    }

    // Bind events directly to qty-down and qty-up buttons
    document.querySelectorAll('.qty-down').forEach(function (btn) {
        btn.addEventListener('click', function (e) {
            e.preventDefault();
            updateQuantity('decrease', this);
        });
    });

    document.querySelectorAll('.qty-up').forEach(function (btn) {
        btn.addEventListener('click', function (e) {
            e.preventDefault();
            updateQuantity('increase', this);
        });
    });
